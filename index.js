const w = width() - 1
const h = height() - 1
const drawGraph = () => {
  for (let x = 0; x < w; x++) {
    const y = EQ(x)

    try {
      addSprite(x, h - Math.floor(y * SCALE), point)
    } catch(e) {}
  }
}

drawGraph()

